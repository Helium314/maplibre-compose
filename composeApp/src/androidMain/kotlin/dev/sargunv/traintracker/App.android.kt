    GoogleMap(
        properties = MapProperties(
            mapStyleOptions = MapStyleOptions(
                if (darkMode) styleJsonDark else styleJsonLight
            ),
        ),
        uiSettings = MapUiSettings(
            zoomControlsEnabled = false,
        )
    ) { }
}

